<template>
    <a-input-search v-model:value="store.query.search" placeholder="Search UserAdmin" style="width: 200px"
        @search="handleSearch" />
</template>

<script setup lang="ts">
import UserAdminFormService from '../../composables/course.composable';
import { container } from 'tsyringe';
import { useCourseStore } from '../../stores/use-Course.store';

const UserAdminService = container.resolve(UserAdminFormService);
const store = useCourseStore();

const handleSearch = () => {
    store.query = { ...store.query, search: store.query.search };
    UserAdminService.fetchPage(store.query);
};
</script>

<style scoped>
.search-UserAdmin {
    margin-bottom: 20px;
}
</style>
